//switch statement
let day = 'thursday';

switch(day) {
  case 'monday':
    console.log('today is monday');
    break;
  case 'tuesday':
    console.log('today is tuesday');
    break;
  case 'wednesday':
    console.log('today is wednesday');
    break;
  default:
    console.log('the week is over as far as Im concerned');
}

